# Thread-safe storage for pending commands: {command_id: command_dict}
_pending_commands = {}
_pending_lock = threading.Lock()

# Session token read from COMM_DIR/session_token at startup
_session_token = None


def write_error_response(command_id, error_msg):
    """Write an error response file so the MCP server doesn't hang at the 45s timeout."""
    try:
        resp_file = COMM_DIR / f"response_{command_id}.json"
        tmp_file = COMM_DIR / f"response_{command_id}.tmp"
        fd = os.open(str(tmp_file), os.O_WRONLY | os.O_CREAT | os.O_TRUNC, 0o600)
        try:
            os.write(fd, json.dumps({"success": False, "error": str(error_msg)}).encode())
        finally:
            os.close(fd)
        os.replace(tmp_file, resp_file)
    except Exception:
        logger.exception("Failed to write error response for command %s", command_id)


class CommandEventHandler(adsk.core.CustomEventHandler):
    """Handles command execution on the main thread via CustomEvent dispatch."""

    def __init__(self):
        super().__init__()

    def notify(self, args):
        command_id = None
        try:
            event_args = adsk.core.CustomEventArgs.cast(args)
            command_id = event_args.additionalInfo

            with _pending_lock:
                command = _pending_commands.pop(command_id, None)

            if command is None:
                return

            result = execute_command(command)
            resp_file = COMM_DIR / f"response_{command_id}.json"
            tmp_file = COMM_DIR / f"response_{command_id}.tmp"
            fd = os.open(str(tmp_file), os.O_WRONLY | os.O_CREAT | os.O_TRUNC, 0o600)
            try:
                os.write(fd, json.dumps(result).encode())
            finally:
                os.close(fd)
            os.replace(tmp_file, resp_file)
        except Exception as e:
            logger.exception("Event handler error for command %s", command_id)
            if command_id:
                write_error_response(command_id, f"Event handler error: {e}")


def _load_session_token():
    """Load session token from COMM_DIR/session_token."""
    global _session_token
    token_path = COMM_DIR / "session_token"
    try:
        if token_path.exists():
            _session_token = token_path.read_text().strip()
    except Exception:
        logger.exception("Failed to load session token from %s", token_path)
        _session_token = None


def run(context):
    global app, ui, monitor_thread, custom_event, custom_event_handler
    try:
        app = adsk.core.Application.get()
        ui = app.userInterface
        COMM_DIR.mkdir(mode=0o700, exist_ok=True)
        os.chmod(COMM_DIR, 0o700)
        _stop_event.clear()

        # Load session token generated by the MCP server
        _load_session_token()

        # Register custom event for main-thread dispatch
        custom_event = app.registerCustomEvent(CUSTOM_EVENT_ID)
        custom_event_handler = CommandEventHandler()
        custom_event.add(custom_event_handler)

        monitor_thread = threading.Thread(target=monitor_commands, daemon=True)
        monitor_thread.start()
        ui.messageBox("Fusion MCP Started!\n\nListening at:\n" + str(COMM_DIR))
    except Exception:
        if ui:
            ui.messageBox("Failed:\n" + traceback.format_exc())


def stop(context):
    global ui, custom_event, custom_event_handler, monitor_thread
    try:
        _stop_event.set()
        if monitor_thread and monitor_thread.is_alive():
            monitor_thread.join(timeout=1.0)
        if custom_event and custom_event_handler:
            custom_event.remove(custom_event_handler)
        if app:
            app.unregisterCustomEvent(CUSTOM_EVENT_ID)
        if ui:
            ui.messageBox("Fusion MCP Stopped")
    except Exception:
        logger.exception("Error during add-in stop")


def _handle_cmd_error(cmd_file, error_msg):
    """Handle a failed command: write error response and clean up the command file."""
    cmd_id = cmd_file.stem.removeprefix("command_")
    write_error_response(cmd_id, error_msg)
    try:
        cmd_file.unlink()
    except Exception:
        pass


def monitor_commands():
    """Background thread: watches for command files, dispatches to main thread via CustomEvent."""
    dispatched_ids = {}  # command_id -> dispatch_time (for TTL-based cleanup)
    while not _stop_event.is_set():
        try:
            # TTL cleanup: remove entries older than 60s to prevent unbounded growth
            now = time.monotonic()
            expired = [cid for cid, t in dispatched_ids.items() if now - t > 60]
            for cid in expired:
                dispatched_ids.pop(cid, None)

            cmd_files = list(COMM_DIR.glob("command_*.json"))
            for cmd_file in cmd_files:
                try:
                    with open(cmd_file, "r") as f:
                        command = json.load(f)

                    command_id = str(command.get("id", ""))
                    if not command_id:
                        _handle_cmd_error(cmd_file, "Command missing 'id' field")
                        continue

                    # Session token validation (MT-3) — reject if token loaded but mismatched
                    if _session_token is not None:
                        cmd_token = command.get("session_token", "")
                        if not hmac.compare_digest(cmd_token.encode(), _session_token.encode()):
                            # Token mismatch — re-read in case MCP server restarted with new token
                            _load_session_token()
                            if _session_token is not None and hmac.compare_digest(cmd_token.encode(), _session_token.encode()):
                                pass  # Token refreshed and now matches — continue processing
                            else:
                                logger.warning("Session token mismatch for command %s", command_id)
                                _handle_cmd_error(cmd_file, "Invalid or missing session token")
                                continue
                    else:
                        # Token not loaded — reject commands for security
                        logger.warning("Session token not loaded — rejecting command %s", command_id)
                        _handle_cmd_error(cmd_file, "Session token not loaded — restart add-in after MCP server")
                        continue

                    # Skip if already dispatched (file deletion may have failed)
                    if command_id in dispatched_ids:
                        continue

                    # Stash command for main-thread handler, then fire event
                    with _pending_lock:
                        _pending_commands[command_id] = command

                    app.fireCustomEvent(CUSTOM_EVENT_ID, command_id)
                    dispatched_ids[command_id] = time.monotonic()

                    try:
                        cmd_file.unlink()
                    except Exception:
                        pass

                except json.JSONDecodeError as e:
                    _handle_cmd_error(cmd_file, f"Malformed command JSON: {e}")
                except Exception as e:
                    _handle_cmd_error(cmd_file, f"Command processing error: {e}")
        except Exception:
            logger.exception("Error in monitor_commands loop")
        _stop_event.wait(0.1)


