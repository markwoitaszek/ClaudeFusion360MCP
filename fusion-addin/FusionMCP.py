def handle_draft(design, rootComp, params):
    body, err = _get_body(rootComp, params)
    if err:
        return err

    face_coll, err = _collect_faces(body, params.get("faces"))
    if err:
        return err

    pull_dir = adsk.core.Vector3D.create(params.get("pull_x", 0), params.get("pull_y", 0), params.get("pull_z", 1))
    plane = adsk.core.Plane.create(adsk.core.Point3D.create(0, 0, 0), pull_dir)

    drafts = rootComp.features.draftFeatures
    draftInput = drafts.createInput(face_coll, plane, False)
    draftInput.angle = adsk.core.ValueInput.createByReal(math.radians(params["angle"]))
    draft = drafts.add(draftInput)
    return {"success": True, "feature_name": draft.name}


def handle_combine(design, rootComp, params):
    target_idx = params["target_body"]
    tool_indices = params["tool_bodies"]
    operation_name = params.get("operation", "cut")
    keep_tools = params.get("keep_tools", False)

    if rootComp.bRepBodies.count == 0:
        return {"success": False, "error": "No bodies in design"}

    ops_map = {
        "cut": adsk.fusion.FeatureOperations.CutFeatureOperation,
        "join": adsk.fusion.FeatureOperations.JoinFeatureOperation,
        "intersect": adsk.fusion.FeatureOperations.IntersectFeatureOperation,
    }
    op = ops_map.get(operation_name)
    if op is None:
        return {"success": False, "error": f"Invalid operation: {operation_name}. Must be cut, join, or intersect"}

    if target_idx < 0 or target_idx >= rootComp.bRepBodies.count:
        return {"success": False, "error": f"Invalid target_body: {target_idx}"}

    target = rootComp.bRepBodies.item(target_idx)
    tools = adsk.core.ObjectCollection.create()
    for idx in tool_indices:
        if idx < 0 or idx >= rootComp.bRepBodies.count:
            return {"success": False, "error": f"Invalid tool body index: {idx}"}
        tools.add(rootComp.bRepBodies.item(idx))

    combines = rootComp.features.combineFeatures
    combineInput = combines.createInput(target, tools)
    combineInput.operation = op
    combineInput.isKeepToolBodies = keep_tools
    combine = combines.add(combineInput)
    return {"success": True, "feature_name": combine.name}


def handle_pattern_rectangular(design, rootComp, params):
    body, err = _get_body(rootComp, params)
    if err:
        return err

    entities = adsk.core.ObjectCollection.create()
    entities.add(body)

    x_axis = rootComp.xConstructionAxis
    y_axis = rootComp.yConstructionAxis

    patterns = rootComp.features.rectangularPatternFeatures
    patInput = patterns.createInput(entities, x_axis)
    patInput.quantityOne = adsk.core.ValueInput.createByString(str(params["x_count"]))
    patInput.distanceOne = adsk.core.ValueInput.createByReal(params["x_spacing"] * (params["x_count"] - 1))

    if params.get("y_count", 1) > 1:
        patInput.setDirectionTwo(y_axis)
        patInput.quantityTwo = adsk.core.ValueInput.createByString(str(params["y_count"]))
        patInput.distanceTwo = adsk.core.ValueInput.createByReal(params["y_spacing"] * (params["y_count"] - 1))

    pattern = patterns.add(patInput)
    return {"success": True, "feature_name": pattern.name}


