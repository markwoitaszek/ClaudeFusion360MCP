# Safe directories for export/import (same list as MCP server)
# SYNC: Keep in sync with mcp-server/validation.py SAFE_EXPORT_DIRS
_SAFE_EXPORT_DIRS = [
    (Path.home() / "Desktop").resolve(),
    (Path.home() / "Downloads").resolve(),
    (Path.home() / "Documents").resolve(),
]


def _validate_export_path(filepath):
    """Validate export/import filepath is within safe directories."""
    if not filepath or not filepath.strip():
        return None, {"success": False, "error": "Filepath cannot be empty"}
    if "\x00" in filepath:
        return None, {"success": False, "error": "Filepath contains null bytes"}
    resolved = Path(filepath).expanduser().resolve()
    is_safe = any(resolved.is_relative_to(d) for d in _SAFE_EXPORT_DIRS)
    if not is_safe:
        return None, {"success": False, "error": "Filepath outside allowed directories (~/Desktop, ~/Downloads, ~/Documents)"}
    return str(resolved), None


def _get_active_sketch(design):
    """Get the active sketch or return an error dict."""
    activeEdit = design.activeEditObject
    if not activeEdit:
        return None, {"success": False, "error": "No active sketch"}
    if not isinstance(activeEdit, adsk.fusion.Sketch):
        return None, {"success": False, "error": "Active edit context is not a sketch. Call finish_sketch or close the active editor first."}
    return activeEdit, None


def _collect_edges(body, indices=None):
    """Collect edges by index list, or all edges if indices is None."""
    edges = adsk.core.ObjectCollection.create()
    if indices is not None:
        for idx in indices:
            if idx < 0 or idx >= body.edges.count:
                return None, {"success": False, "error": f"Invalid edge index: {idx}. Available: 0-{body.edges.count - 1}"}
            edges.add(body.edges.item(idx))
    else:
        for edge in body.edges:
            edges.add(edge)
    return edges, None


def _collect_faces(body, indices=None):
    """Collect faces by index list, or all faces if indices is None."""
    faces = adsk.core.ObjectCollection.create()
    if indices is not None:
        for idx in indices:
            if idx < 0 or idx >= body.faces.count:
                return None, {"success": False, "error": f"Invalid face index: {idx}. Available: 0-{body.faces.count - 1}"}
            faces.add(body.faces.item(idx))
    else:
        for face in body.faces:
            faces.add(face)
    return faces, None


def _get_joint(rootComp, params):
    """Get a joint by index or return an error dict."""
    joint_index = params.get("joint_index", 0)
    if joint_index < 0 or joint_index >= rootComp.joints.count:
        return None, {"success": False, "error": f"Invalid joint_index: {joint_index}. Available: 0-{rootComp.joints.count - 1}"}
    return rootComp.joints.item(joint_index), None


