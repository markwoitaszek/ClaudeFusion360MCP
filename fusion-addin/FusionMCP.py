def _get_body(rootComp, params):
    """Helper: resolve body from params. Returns (body, error_dict_or_None)."""
    body_index = params.get("body_index")
    if body_index is not None:
        if body_index < 0 or body_index >= rootComp.bRepBodies.count:
            return None, {"success": False, "error": f"Invalid body_index: {body_index}. Available: 0-{rootComp.bRepBodies.count - 1}"}
        return rootComp.bRepBodies.item(body_index), None
    if rootComp.bRepBodies.count == 0:
        return None, {"success": False, "error": "No bodies in design"}
    return rootComp.bRepBodies.item(rootComp.bRepBodies.count - 1), None


